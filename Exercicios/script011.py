n1 = int(input('Digite um número:'))
n2 = int(input('Digite outro número:'))
adi = n1 + n2
sub = n1 - n2
mul = n1 * n2
div = n1 / n2
di = n1 // n2
res = n1 % n2
print('A adição é {}, a subtração é {}, a multiplicação é {} e a divisão é {}.'. format(adi, sub, mul, div), end=' ')
print('A divisão inteira e o resto da divisão são, respectivamente {} e {}.'.format(di, res))






